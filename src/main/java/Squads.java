public class Squads{
    private String mName;
    private String mCause;

    public Squads(String mName, String mCause) {
        this.mName = mName;
        this.mCause = mCause;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmCause() {
        return mCause;
    }

    public void setmCause(String mCause) {
        this.mCause = mCause;
    }
}
